// 1. Number

let num1 = 27;
let num2 = 27.1;

let inf = Infinity;
let mInf = -Infinity;
// NaN: Not a Number
let nan = NaN;

// 2. String
let str1 = "Hello";
let str2 = 'World';
// Template Literal 문법
let str3 = `Hello ${str1} ${str2}`;

console.log(str1 + str2);
console.log(str3);

// 3. Boolean
let isSwitchOn = true;
let isSwitchOff = false;

console.log(isSwitchOn);
console.log(isSwitchOff);

// 4. Null
// null은 값이 없음을 의미
let nullValue = null;
console.log(nullValue);

// 5. Undefined
// undefined는 값이 정의되지 않은 상태를 의미
let undefinedValue = undefined;
console.log(undefinedValue);

